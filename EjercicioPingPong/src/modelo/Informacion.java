package modelo;

public class Informacion {
	private String nombre;
	private String alias;
	private int edad;
	private String ciudad;
	private String liga;
	private String lateralidad;
	private String golpeEstrella;
	private String ataque;
	private String defensa;
	private String gif;
	private String grito;

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getAlias() {
		return alias;
	}

	public void setAlias(String alias) {
		this.alias = alias;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public String getCiudad() {
		return ciudad;
	}

	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}

	public String getLiga() {
		return liga;
	}

	public void setLiga(String liga) {
		this.liga = liga;
	}

	public String getLateralidad() {
		return lateralidad;
	}

	public void setLateralidad(String lateralidad) {
		this.lateralidad = lateralidad;
	}

	public String getGolpeEstrella() {
		return golpeEstrella;
	}

	public void setGolpeEstrella(String golpeEstrella) {
		this.golpeEstrella = golpeEstrella;
	}

	public String getAtaque() {
		return ataque;
	}

	public void setAtaque(String ataque) {
		this.ataque = ataque;
	}

	public String getDefensa() {
		return defensa;
	}

	public void setDefensa(String defensa) {
		this.defensa = defensa;
	}

	public String getGif() {
		return gif;
	}

	public void setGif(String gif) {
		this.gif = gif;
	}

	public String getGrito() {
		return grito;
	}

	public void setGrito(String grito) {
		this.grito = grito;
	}

}
